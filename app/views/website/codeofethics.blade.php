<h4 class="head-title">Code of Ethics</h4>
<h4>Code of Ethics for Contractors</h4>

<ol>
	<li>
		<h4>INTRODUCTION</h4>
		<p>The construction industry has an important and unique role in national economic development.The construction industry remains the main catalyst for the development of other key economic sectors, particularly the service, manufacturing and other sectors.</p>

		<p>The success of the construction industry is very much determined by the level of efficiency, productivity, quality and professionalism in its service delivery. The delivery of construction services is directly or closely related to the credibility of the contractors in carrying out their responsibilities.</p>

		<p>The government has given special attention to the enhancement of the productivity, image and professionalism of the construction industry as part of its strategic thrusts to raise the construction industry to greater heights. Towards this end, the Code of Ethics for Contractors has been drafted to further strengthen the adoption of noble values and ethics among contractors in the country.</p>

		<p>This code of ethics is dynamic in nature; it may undergo changes whereby its contents may be amended, added or deleted according to the requirements. Any changes to the contents require the approval of the Board, which comprises of various stakeholders in the construction industry.</p>
	</li>

	<div class="clear"></div>

	<li>
		<h4>IMPLEMENTATION OBJECTIVES OF THE CODE OF ETHICS FOR CONTRACTORS</h4>
		<p>This code of ethics was formulated, instituted and implemented with the following objectives:</p>
		<ol type="i">
			<li><p>To outline best practices as well as noble conduct which are accepted as standard practices among contractors that are capable of motivating and enhancing the level of professionalism, integrity and accountability;</p></li>
			<li><p>To inculcate the self-regulatory approach among contractors in order to boost the level of public confidence in the industry;</p></li>
		</ol>
	</li>

	<div class="clear"></div>

	<li>
		<h4>APPLICATION OF THE CODE OF ETHICS FOR CONTRACTORS</h4>
		<ol type="i">
			<li><p>This code of ethics shall be accepted and adopted by all contractors in the country, whether local or foreign.</p></li>
			<li><p>All contractors are obliged to abide by this code of ethics in order to uphold their personal honour, integrity and goodwill in the construction industry.</p></li>
		</ol>
	</li>

	<div class="clear"></div>

	<li>
		<h4>CONTENTS OF THE CODE OF ETHICS FOR CONTRACTORS</h4>
		<p>The Code of Ethics for Contractors outlines the ethics or noble practices based on the following 6 key principles:</p>
			<p>Principle 1: Honesty in Carrying out responsibilities<br />
			Principle 2: Compliance with the Laws and Regulations<br />
			Principle 3: Respect for the Individual and Community<br />
			Principle 4: Importance of Quality, Skills and Standards<br />
			Principle 5: Importance of Safety and Health<br />
			Principle 6: Importance of Environmental Preservation
		</p>

		<div class="clear"></div>

		<h4>4.1&nbsp;&nbsp;PRINCIPLE 1: HONESTY IN CARRYING OUT RESPONSIBILITIES</h4>
			<ol type="1">
				<li><p>Contractors must carry out construction works with integrity, honesty and responsibility and in an equitable, fair and excellent manner with the aim of fulfilling customer satisfaction through the attainment of a level of quality that has been agreed upon or set.</p></li>
				
				<li><p>Contractors must resist, refrain and report any form of bribery which includes giving or acceptance of specific payments or unethical behavior that is rejected by society in any procurement process and implementation thereof.</p></li>
				
				<li><p>Contractors must place the aspect of trust as the driving force in implementing construction works properly and with the utmost responsibility.</p></li>
				
				<li><p>Contractors must operate their business in a manner that reflects a good image as well as a healthy picture of the company, the association, the construction industry and the country, in general.</p></li>
				
				<li><p>Contractors must give priority to carry out construction works under their own commitment and avoid the provision of works in total or repeatedly which may affect the work delivery, quality of service or construction product. All provision of works must be monitored closely to maintain the quality of service.</p></li>
				
				<li><p>Contractors must refrain from verifying the implementation of the construction works of other contractors or subcontractors unless they themselves have managed or closely monitored the services.</p></li>
				
				<li><p>Contractors must refrain from using falsified documents or misleading advertisements for any purposes pertaining to the procurement of a license or contract as well as the implementation of construction works.</p></li>
				
				<li><p>Contractors must respect the sensitivity and confidentiality of information obtained during the provision of the construction service and must not disclose any information pertaining to the construction works which may threaten the interests of any parties.</p></li>
				
				<li><p>Contractors must inform their clients of any conflict of interest which may arise during the tendering process, designing and implementation of the construction project.</p></li>
				
				<li><p>Contractors must refrain from making statements that are misleading or defamatory in nature with the intention of creating confusion or negative impact on any individual or organisation or other related parties.</p></li>
				
				<li><p>Contractors must declare truthfully the work in hand information while submitting the bids or before being awarded another work.</p></li>
				
				<li><p>Contractors shall not lease his certificate to another certificate holder or to any non-licensee for any procurement of works.</p></li>
				
				<li><p>Contractor shall not issue letter of authorization in favor of an employee of another firm for the purpose of tendering or execution of works.</p></li>
				
				<li><p>Contractor shall not undertake or implement any project or constructions work which is not within their class or category and the area of specialization, in respect of which the certificate is issued.</p></li>
				
				<li><p>Contractors must provide right information/address to their client and competent authority.</p></li>
			</ol>
		</li>

		<div class="clear"></div>

		<h4>4.2&nbsp;&nbsp;PRINCIPLE 2: COMPLIANCE WITH THE LAWS AND REGULATIONS</h4>
		<p>Contractors who are registered with professional bodies in the construction industry must comply with and adopt all regulations, guidelines and decisions prescribed by the competent authority at all times.</p>

		<ol>
			<li><p>Contractors must utilize the services of sub-contractors who are competent and legally registered and employ skilled and competent construction workers who are recognized by Competent Authorities. All contractors may sub-contract upon prior approval from the client within the limit specified in PRR.</p></li>
			
			<li><p>Contractors must give priority to the recruitment of local construction workers and must not use the services of illegal foreign workers. These workers must not be under age. All employees should be recruited with formal agreement.</p></li>
			
			<li><p>Contractors must understand and comply with all relevant laws and regulations pertaining to construction works and the industry.</p></li>
			
			<li><p>Contractors must refrain from being involved, directly or indirectly, with their clients (project owners, suppliers of building material and machinery, financiers and others) who are known as entities engaged in operations that are fraudulent in nature or other activities that are illegal according to the laws of the country.</p></li>
			
			<li><p>Contractors must give due respect to all the parties involved in a construction project including respect the values, history, religion, beliefs and culture of the local community throughout the construction period.</p></li>
		</ol>

		<div class="clear"></div>

		<h4>4.3&nbsp;&nbsp;PRINCIPLE 3: RESPECT FOR THE INDIVIDUAL AND COMMUNITY</h4>
		<ol>
			<li><p>Contractors must refrain from discriminating on the basis of race, age, gender, state and country of origin, capability or sexual orientation during the implementation of all construction activities.</p></li>
			<li><p>Contractors must ensure that each worker is provided with a satisfactory working environment, fairly treated, equitably rewarded and provided with due assistance in terms of career development and welfare.</p></li>
		</ol>

		<div class="clear"></div>

		<h4>4.4&nbsp;&nbsp;PRINCIPLE 4: IMPORTANCE OF QUALITY, SKILLS AND STANDARDS</h4>
		<ol>
			<li><p>Contractors must endeavour to further maintain, strengthen and enhance their efficiency, expertise and professionalism as well as to provide the same support to their workers.</p></li>
			
			<li><p>Contractors must provide sufficient numbers of skilled workforce to complete a construction project according to the contract agreed upon.</p></li>
			
			<li><p>Contractors must complete all construction projects according to the construction industry standards that are accepted by all parties.</p></li>
			
			<li><p>Contractors must refrain from accepting construction projects that are beyond their capabilities and instead concentrate on projects that are related to their fields of expertise and capabilities including financial capability.</p></li>
			
			<li><p>Contractors must use materials and equipments that meet the quality prescribed and must not attempt to use materials and hardware that are no longer approved for use.</p></li>
		</ol>

		<div class="clear"></div>

		<h4>4.5&nbsp;&nbsp;PRINCIPLE 5: IMPORTANCE OF SAFETY, HEALTH & WELFARE</h4>
		<ol>
			<li><p>Contractors must give priority to the aspects of safety and health at the workplace and surrounding to ensure that the construction works and their outcomes do not endanger (diseases, injuries or deaths) any individual who is directly involved with the construction works, the community around the construction site, etc.</p></li>
			
			<li><p>Contractors must pay attention to the welfare of their workers on humanitarian grounds in all circumstances throughout the period of their service as required by the law.</p></li>

			<li><p>Contractors must follow the minimum standard of OHS Rules prescribed by MoLHR for all time.</p></li>
		</ol>

		<div class="clear"></div>

		<h4>4.6&nbsp;&nbsp;PRINCIPLE 6: IMPORTANCE OF ENVIRONMENTAL PRESERVATION</h4>
		<ol>
			<li><p>Contractors must carry out their construction works with utmost consideration to the environment by complying with all the procedures set by the relevant government agencies. This includes taking due action to avoid wastage of natural resources, irresponsible disposal of excess building materials or other action that may result in environmental damage.</p></li>
			
			<li><p>All contractors must have their own internal policy on this principle if it is not obligatory under any legal provisions.</p></li>
		</ol>
</ol>

